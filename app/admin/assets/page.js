/* ── Homepage Card Preview (uses real homepage CSS classes) ── */

function CardPreview({ event }) {
  const videoRef = useRef(null);
  const [videoReady, setVideoReady] = useState(false);
  const [tooltip, setTooltip] = useState({ visible: false, text: '', x: 0, y: 0 });
  const selectedImage = event.selectedImageCandidate?.url || event.imageCandidates?.find(c => c.selected)?.url;
  const asset = event.latestAsset;
  const imgSrc = selectedImage || asset?.portraitUrl || '';
  const videoSrc = asset?.animationUrl || null;
  const bgUrl = imgSrc ? (imgSrc.startsWith('http') ? imgSrc : '/' + imgSrc.replace(/^\//, '')) : '';

  const dayName = (() => {
    if (!event.date) return 'MONDAY';
    const DAYS = ['SUNDAY', 'MONDAY', 'TUESDAY', 'WEDNESDAY', 'THURSDAY', 'FRIDAY', 'SATURDAY'];
    if (DAYS.includes(event.date)) return event.date;
    try { const d = new Date(event.date + 'T12:00:00Z'); return DAYS[d.getUTCDay()] || 'MONDAY'; } catch { return 'MONDAY'; }
  })();

  const mode = event.mode || 'day';
  const tooltipText = mode === 'night'
    ? `🌙 ${event.startTime || ''} · ${event.venue || ''}`
    : `☀️ ${event.startTime || ''} · ${event.venue || ''}`;

  useEffect(() => {
    if (!videoSrc || !videoRef.current) return;
    const v = videoRef.current;
    const onReady = () => setVideoReady(true);
    v.addEventListener('canplay', onReady, { once: true });
    v.preload = 'auto'; v.load();
    return () => v.removeEventListener('canplay', onReady);
  }, [videoSrc]);

  function handleMouseEnter(e) {
    if (videoReady && videoRef.current) videoRef.current.play().catch(() => {});
    setTooltip({ visible: true, text: tooltipText, x: e.clientX + 12, y: e.clientY + 12 });
  }
  function handleMouseMove(e) {
    setTooltip(prev => ({ ...prev, x: e.clientX + 12, y: e.clientY + 12 }));
  }
  function handleMouseLeave() {
    if (videoRef.current) { videoRef.current.pause(); videoRef.current.currentTime = 0; }
    setTooltip(prev => ({ ...prev, visible: false }));
  }

  return (
    <>
      <div className="card-preview-wrap" onMouseEnter={handleMouseEnter} onMouseMove={handleMouseMove} onMouseLeave={handleMouseLeave}>
        <div className="card" style={{ height: '100%' }}>
          <div className="card-img-strip" style={{ height: '100%', position: 'absolute', inset: 0 }}>
            <div className="card-img" style={bgUrl ? { backgroundImage: `linear-gradient(180deg, rgba(0,0,0,1) 0%, rgba(0,0,0,0) 59%), url('${bgUrl}')`, height: '100%' } : { height: '100%' }}>
              {videoSrc && <video ref={videoRef} className={`card-video ${videoReady ? 'is-ready' : ''}`} src={videoSrc} muted loop playsInline preload="none" />}
            </div>
          </div>
          <div className="card-content-strip" style={{ height: '100%', position: 'absolute', inset: 0, zIndex: 3 }}>
            <a href="#" className="card-content" onClick={e => e.preventDefault()} style={{ height: '100%', justifyContent: 'flex-start' }}>
              <span className="card-day">{dayName}</span>
              <span className="card-venue">{event.title || event.venue || ''}</span>
              <span className="card-city">{event.city || ''}</span>
            </a>
          </div>
        </div>
      </div>
      <div className={`tooltip ${tooltip.visible ? 'visible' : ''}`} style={{ left: tooltip.x, top: tooltip.y, position: 'fixed', zIndex: 9999 }}>{tooltip.text}</div>
    </>
  );
}

/* ── Edit Panel ── */

function EditPanel({ event, onUpdate, onSendToPublish }) {
  const [form, setForm] = useState({});
  const [saving, setSaving] = useState(false);
  const [sending, setSending] = useState(false);
  const [error, setError] = useState(null);
  const [cropCandidate, setCropCandidate] = useState(null);
  const [dropImage, setDropImage] = useState(null);
  const [dropOver, setDropOver] = useState(false);
  const [uploading, setUploading] = useState(false);
  const dropRef = useRef(null);

  useEffect(() => {
    if (event) setForm({
      title: event.title || '', venue: event.venue || '', city: event.city || '',
      date: event.date || '', startTime: event.startTime || '', endTime: event.endTime || '',
      mode: event.mode || 'day', description: event.description || '',
    });
  }, [event?.id]);

  // Global paste listener
  useEffect(() => {
    function onPaste(e) {
      const items = e.clipboardData?.items;
      if (!items) return;
      for (const item of items) {
        if (item.type.startsWith('image/')) {
          e.preventDefault();
          const file = item.getAsFile();
          if (file) {
            const reader = new FileReader();
            reader.onload = () => setDropImage(reader.result);
            reader.readAsDataURL(file);
          }
          return;
        }
      }
    }
    document.addEventListener('paste', onPaste);
    return () => document.removeEventListener('paste', onPaste);
  }, [event?.id]);

  if (!event) return <div className="ep-empty">← Select an event from the queue</div>;

  function field(key, label, opts = {}) {
    return (
      <div className="ep-field">
        <label>{label}</label>
        {opts.select ? (
          <select value={form[key] || ''} onChange={e => setForm(f => ({ ...f, [key]: e.target.value }))}>
            <option value="day">☀️ Day</option>
            <option value="night">🌙 Night</option>
            <option value="family">👨‍👩‍👧 Family</option>
            <option value="grownup">☀️ Grown-up</option>
          </select>
        ) : opts.textarea ? (
          <textarea value={form[key] || ''} onChange={e => setForm(f => ({ ...f, [key]: e.target.value }))} rows={3} />
        ) : (
          <input value={form[key] || ''} onChange={e => setForm(f => ({ ...f, [key]: e.target.value }))} />
        )}
      </div>
    );
  }

  async function save() {
    setSaving(true); setError(null);
    try {
      const res = await fetch(`/api/events/${event.id}`, {
        method: 'PATCH', headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify(form),
      });
      if (!res.ok) throw new Error((await res.json()).error ?? 'Save failed');
      onUpdate?.();
    } catch (err) { setError(err.message); }
    finally { setSaving(false); }
  }

  async function handleSendToPublish() {
    setSending(true); setError(null);
    try {
      // Save metadata first
      await fetch(`/api/events/${event.id}`, {
        method: 'PATCH', headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify(form),
      });
      // Approve to approved_2 (ready for publish calendar)
      const res = await fetch(`/api/events/${event.id}/review`, {
        method: 'POST', headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ action: 'approve', stage: 2, reviewedBy: 'admin' }),
      });
      if (!res.ok) throw new Error((await res.json()).error);
      onSendToPublish?.(event.id);
      onUpdate?.();
    } catch (err) { setError(err.message); }
    finally { setSending(false); }
  }

  async function handleSelectImage(candidate) {
    setCropCandidate(candidate);
  }

  async function confirmSelectImage(candidateId, cropOffsetX) {
    try {
      const res = await fetch(`/api/events/${event.id}/select-image`, {
        method: 'POST', headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ candidateId, cropOffsetX }),
      });
      if (!res.ok) throw new Error((await res.json()).error);
      setCropCandidate(null);
      onUpdate?.();
    } catch (err) { alert('Select failed: ' + err.message); }
  }

  function handleDropFile(file) {
    const reader = new FileReader();
    reader.onload = () => setDropImage(reader.result);
    reader.readAsDataURL(file);
  }

  function onDragOver(e) { e.preventDefault(); setDropOver(true); }
  function onDragLeave() { setDropOver(false); }
  function onDrop(e) {
    e.preventDefault(); setDropOver(false);
    const file = e.dataTransfer.files?.[0];
    if (file?.type.startsWith('image/')) handleDropFile(file);
  }

  async function confirmDropImage(cropOffsetX) {
    if (!dropImage) return;
    setUploading(true);
    try {
      // Upload as base64
      const blob = await fetch(dropImage).then(r => r.blob());
      const form = new FormData();
      form.append('image', blob, 'pasted-image.png');
      const uploadRes = await fetch(`/api/events/${event.id}/upload-image`, { method: 'POST', body: form });
      if (!uploadRes.ok) throw new Error((await uploadRes.json()).error);
      const uploadData = await uploadRes.json();

      // Select it immediately
      const newId = uploadData.candidate?.id;
      if (newId) {
        await fetch(`/api/events/${event.id}/select-image`, {
          method: 'POST', headers: { 'Content-Type': 'application/json' },
          body: JSON.stringify({ candidateId: newId, cropOffsetX }),
        });
      }
      setDropImage(null);
      onUpdate?.();
    } catch (err) { alert('Upload failed: ' + err.message); }
    finally { setUploading(false); }
  }

  return (
    <div className="ep">
      <div className="ep-top">
        <CardPreview event={event} />
        <div className="ep-meta">
          <div className="ep-form">
            {field('title', 'Title')}
            <div className="ep-row">
              {field('venue', 'Venue')}
              {field('city', 'City')}
            </div>
            <div className="ep-row">
              {field('date', 'Date')}
              {field('startTime', 'Start')}
              {field('endTime', 'End')}
            </div>
            {field('mode', 'Mode', { select: true })}
            {field('description', 'Description', { textarea: true })}
          </div>

          <button className="ep-save" onClick={save} disabled={saving}>{saving ? 'Saving…' : '💾 Save'}</button>
          {error && <div className="ep-error">⚠ {error}</div>}

          {(() => {
            const hasImage = event.imageCandidates?.some(c => c.selected) || event.selectedImageCandidate?.url;
            return (
              <button className="ep-publish" onClick={handleSendToPublish} disabled={sending || !hasImage}>
                {sending ? '⏳ Sending…' : !hasImage ? '⚠ Select an image first' : '📅 Send to Publish Calendar'}
              </button>
            );
          })()}
        </div>
      </div>

      <ImageGallery event={event} onUpdate={onUpdate} onSelectImage={handleSelectImage}
        dropImage={dropImage} setDropImage={setDropImage} dropOver={dropOver}
        onDragOver={onDragOver} onDragLeave={onDragLeave} onDropFile={onDrop}
        onConfirmDrop={() => confirmDropImage(50)} uploading={uploading}
      />
      {cropCandidate && <CropModal candidate={cropCandidate} onSelect={confirmSelectImage} onClose={() => setCropCandidate(null)} />}
    </div>
  );
}


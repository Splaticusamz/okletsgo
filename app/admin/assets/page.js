/* ── Crop Modal (expanded) ── */
function CropModal({ candidate, onSelect, onClose }) {
  const [dragState, setDragState] = useState(null);
  const [offsetX, setOffsetX] = useState(50);
  const [aspectRatio, setAspectRatio] = useState('1:2');
  const imgRef = useRef(null);
  const boxRef = useRef(null);

  const aspects = [
    { label: '1:2 Portrait', value: '1:2', ratio: 0.5 },
    { label: '1:1 Square', value: '1:1', ratio: 1 },
    { label: '9:16 Stories', value: '9:16', ratio: 9 / 16 },
    { label: '16:9 Landscape', value: '16:9', ratio: 16 / 9 },
    { label: '4:5 Instagram', value: '4:5', ratio: 4 / 5 },
  ];
  const currentAspect = aspects.find(a => a.value === aspectRatio) || aspects[0];

  function handlePointerDown(e) {
    e.preventDefault();
    const box = boxRef.current?.getBoundingClientRect();
    if (!box) return;
    setDragState({ startX: e.clientX, startOffset: offsetX, boxWidth: box.width });
    boxRef.current?.setPointerCapture(e.pointerId);
  }
  function handlePointerMove(e) {
    if (!dragState) return;
    const img = imgRef.current;
    if (!img || !img.naturalWidth) return;
    const imgAspect = img.naturalWidth / img.naturalHeight;
    if (imgAspect <= currentAspect.ratio) return;
    const visibleFraction = currentAspect.ratio / imgAspect;
    const maxPan = ((1 - visibleFraction) / 2) * 100;
    const dx = e.clientX - dragState.startX;
    const pxToPercent = (dx / dragState.boxWidth) * 100 * (imgAspect / currentAspect.ratio);
    setOffsetX(Math.max(50 - maxPan, Math.min(50 + maxPan, dragState.startOffset - pxToPercent)));
  }
  function handlePointerUp() { setDragState(null); }

  return (
    <div className="crop-backdrop" onClick={onClose}>
      <div className="crop-modal" onClick={e => e.stopPropagation()}>
        <button className="crop-close" onClick={onClose}>×</button>
        <div className="crop-title">Crop Image</div>
        <div className="crop-hint">Choose aspect ratio and drag to position</div>

        {/* Aspect ratio selector */}
        <div className="crop-aspects">
          {aspects.map(a => (
            <button key={a.value} className={`crop-aspect-btn ${aspectRatio === a.value ? 'crop-aspect-btn--active' : ''}`} onClick={() => setAspectRatio(a.value)}>
              {a.label}
            </button>
          ))}
        </div>

        <div className="crop-preview-wrap" ref={boxRef} onPointerDown={handlePointerDown} onPointerMove={handlePointerMove} onPointerUp={handlePointerUp} onPointerCancel={handlePointerUp} style={{ cursor: dragState ? 'grabbing' : 'grab' }}>
          <div className="crop-aspect-box" style={{ aspectRatio: currentAspect.ratio }}>
            <img ref={imgRef} src={candidate.url} alt="" draggable={false} style={{ objectPosition: `${offsetX}% 50%` }} />
          </div>
        </div>
        <div className="crop-actions">
          <button className="crop-btn crop-btn--cancel" onClick={onClose}>Cancel</button>
          <button className="crop-btn crop-btn--confirm" onClick={() => onSelect(candidate.id, Math.round(offsetX), aspectRatio)}>✓ Apply Crop</button>
        </div>
      </div>
    </div>
  );
}


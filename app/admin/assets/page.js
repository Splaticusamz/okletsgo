/* ── Source tag helper ── */

function sourceInfo(candidate) {
  const src = candidate.source || candidate.provenance || '';
  if (src === 'venue-search') return { tag: 'Venue photo', icon: '🏢', cls: 'img-tag--venue', desc: candidate.searchQuery ? `Search: "${candidate.searchQuery}"` : 'Found via venue search' };
  if (src === 'event-page') return { tag: 'Event page', icon: '🎫', cls: 'img-tag--event', desc: candidate.sourceUrl ? `Scraped from event listing` : 'From event page' };
  if (src === 'event-search') return { tag: 'Event search', icon: '🔍', cls: 'img-tag--event', desc: candidate.searchQuery ? `Search: "${candidate.searchQuery}"` : 'Found via event search' };
  if (src === 'ai-generated') return { tag: 'AI generated', icon: '🤖', cls: 'img-tag--ai', desc: 'Generated by fal.ai' };
  if (src === 'upload' || src === 'manual') return { tag: 'Uploaded', icon: '📤', cls: 'img-tag--upload', desc: 'Manually uploaded' };
  if (src.startsWith('scraper:') || src.startsWith('tourismkelowna') || src.startsWith('castanet')) return { tag: src.replace('scraper:', ''), icon: '🔗', cls: 'img-tag--scraper', desc: `From ${src} scraper` };
  if (src === 'discord' || src.startsWith('discord:')) return { tag: 'Discord', icon: '💬', cls: 'img-tag--discord', desc: 'Imported from Discord' };
  if (candidate.category === 'venue') return { tag: 'Venue', icon: '🏢', cls: 'img-tag--venue', desc: 'Venue image' };
  if (candidate.category === 'event') return { tag: 'Event', icon: '🎫', cls: 'img-tag--event', desc: 'Event image' };
  if (candidate.category === 'activity') return { tag: 'Activity', icon: '🎨', cls: 'img-tag--ai', desc: 'Activity image' };
  return { tag: 'Image', icon: '📷', cls: '', desc: 'Unknown source' };
}

/* ── Pipeline status helper ── */
function PipelineDots({ candidate }) {
  const steps = [
    { key: 'original', label: 'Original', done: true },
    { key: 'upscaled', label: 'Upscaled', done: !!candidate.upscaled },
    { key: 'cropped', label: 'Cropped', done: !!candidate.cropped },
    { key: 'animated', label: 'Animated', done: !!candidate.animated },
  ];
  return (
    <div className="pipeline-dots">
      {steps.map((s, i) => (
        <React.Fragment key={s.key}>
          <div className={`pipeline-dot ${s.done ? 'pipeline-dot--done' : ''}`} title={s.label} />
          {i < steps.length - 1 && <div className={`pipeline-line ${s.done && steps[i + 1].done ? 'pipeline-line--done' : ''}`} />}
        </React.Fragment>
      ))}
    </div>
  );
}


/* ── Edit Panel (redesigned) ── */
function EditPanel({ event, onUpdate, onSendToPublish }) {
  const [form, setForm] = useState({});
  const [saving, setSaving] = useState(false);
  const [sending, setSending] = useState(false);
  const [error, setError] = useState(null);

  // Source tabs
  const [sourceTab, setSourceTab] = useState(null); // null | 'scrape' | 'upload' | 'generate'

  // Gallery selection
  const [selectedId, setSelectedId] = useState(null); // single-click focus for action panel
  const [checkedIds, setCheckedIds] = useState(new Set()); // multi-select checkboxes
  const [lastCheckedIdx, setLastCheckedIdx] = useState(null);
  const [batchUpscaling, setBatchUpscaling] = useState(false);

  // Modals
  const [cropCandidate, setCropCandidate] = useState(null);
  const [lightbox, setLightbox] = useState(null);

  const candidates = event?.imageCandidates ?? [];
  const selectedCandidate = candidates.find(c => c.id === selectedId) || null;

  useEffect(() => {
    if (event) setForm({
      title: event.title || '', venue: event.venue || '', city: event.city || '',
      date: event.date || '', startTime: event.startTime || '', endTime: event.endTime || '',
      mode: event.mode || 'day', description: event.description || '',
    });
    setSelectedId(null);
    setCheckedIds(new Set());
    setSourceTab(null);
  }, [event?.id]);

  // Refs for keyboard handler (to avoid stale closures)
  const selectedCandidateRef = useRef(null);
  selectedCandidateRef.current = selectedCandidate;
  const checkedIdsRef = useRef(checkedIds);
  checkedIdsRef.current = checkedIds;

  // Keyboard shortcuts
  useEffect(() => {
    if (!event) return;
    function onKey(e) {
      if (e.target.tagName === 'INPUT' || e.target.tagName === 'TEXTAREA' || e.target.tagName === 'SELECT') return;
      const sc = selectedCandidateRef.current;
      if (e.key === 'u' || e.key === 'U') { if (sc) document.querySelector('[data-action="upscale"]')?.click(); }
      if (e.key === 'c' || e.key === 'C') { if (sc) setCropCandidate(sc); }
      if (e.key === 'a' || e.key === 'A') { if (sc) document.querySelector('[data-action="animate"]')?.click(); }
      if (e.key === ' ') { e.preventDefault(); if (sc) setLightbox(sc); }
      if (e.key === 'Delete' || e.key === 'Backspace') {
        // Handled via refs below
      }
      if (e.key === 'Escape') { setSelectedId(null); setCheckedIds(new Set()); setLightbox(null); setCropCandidate(null); }
    }
    document.addEventListener('keydown', onKey);
    return () => document.removeEventListener('keydown', onKey);
  }, [event?.id]);

  // Handle crop click from action panel
  useEffect(() => {
    function handleCropClick(e) {
      const sc = selectedCandidateRef.current;
      if (e.target.closest('[data-action="crop"]') && sc) {
        setCropCandidate(sc);
      }
    }
    document.addEventListener('click', handleCropClick);
    return () => document.removeEventListener('click', handleCropClick);
  }, []);

  if (!event) return <div className="ep-empty">← Select an event from the queue</div>;

  // Category splits
  const venueImgs = candidates.filter(c => c.category === 'venue');
  const eventImgs = candidates.filter(c => c.category === 'event');
  const activityImgs = candidates.filter(c => c.category === 'activity');
  const otherImgs = candidates.filter(c => !c.category);
  const hasCategorized = venueImgs.length > 0 || eventImgs.length > 0 || activityImgs.length > 0;

  function field(key, label, opts = {}) {
    return (
      <div className="ep-field">
        <label>{label}</label>
        {opts.select ? (
          <select value={form[key] || ''} onChange={e => setForm(f => ({ ...f, [key]: e.target.value }))}>
            <option value="day">☀️ Day</option>
            <option value="night">🌙 Night</option>
            <option value="family">👨‍👩‍👧 Family</option>
            <option value="grownup">☀️ Grown-up</option>
          </select>
        ) : opts.textarea ? (
          <textarea value={form[key] || ''} onChange={e => setForm(f => ({ ...f, [key]: e.target.value }))} rows={3} />
        ) : (
          <input value={form[key] || ''} onChange={e => setForm(f => ({ ...f, [key]: e.target.value }))} />
        )}
      </div>
    );
  }

  async function save() {
    setSaving(true); setError(null);
    try {
      const res = await fetch(`/api/events/${event.id}`, { method: 'PATCH', headers: { 'Content-Type': 'application/json' }, body: JSON.stringify(form) });
      if (!res.ok) throw new Error((await res.json()).error ?? 'Save failed');
      onUpdate?.();
    } catch (err) { setError(err.message); }
    finally { setSaving(false); }
  }

  async function handleSendToPublish() {
    setSending(true); setError(null);
    try {
      await fetch(`/api/events/${event.id}`, { method: 'PATCH', headers: { 'Content-Type': 'application/json' }, body: JSON.stringify(form) });
      const res = await fetch(`/api/events/${event.id}/review`, { method: 'POST', headers: { 'Content-Type': 'application/json' }, body: JSON.stringify({ action: 'approve', stage: 2, reviewedBy: 'admin' }) });
      if (!res.ok) throw new Error((await res.json()).error);
      onSendToPublish?.(event.id);
      onUpdate?.();
    } catch (err) { setError(err.message); }
    finally { setSending(false); }
  }

  async function handleSetPrimary(candidate) {
    try {
      const res = await fetch(`/api/events/${event.id}/select-image`, { method: 'POST', headers: { 'Content-Type': 'application/json' }, body: JSON.stringify({ candidateId: candidate.id, cropOffsetX: 50 }) });
      if (!res.ok) throw new Error((await res.json()).error);
      onUpdate?.();
    } catch (err) { alert('Failed: ' + err.message); }
  }

  async function handleRemoveImage(candidateId) {
    try {
      const updated = candidates.filter(c => c.id !== candidateId);
      const res = await fetch(`/api/events/${event.id}`, { method: 'PATCH', headers: { 'Content-Type': 'application/json' }, body: JSON.stringify({ imageCandidates: updated }) });
      if (!res.ok) throw new Error((await res.json()).error);
      if (selectedId === candidateId) setSelectedId(null);
      setCheckedIds(prev => { const n = new Set(prev); n.delete(candidateId); return n; });
      onUpdate?.();
    } catch (err) { alert('Remove failed: ' + err.message); }
  }

  function handleCheck(candidateId, shiftKey) {
    const allIds = candidates.map(c => c.id);
    const idx = allIds.indexOf(candidateId);

    setCheckedIds(prev => {
      const next = new Set(prev);
      if (shiftKey && lastCheckedIdx !== null) {
        const start = Math.min(lastCheckedIdx, idx);
        const end = Math.max(lastCheckedIdx, idx);
        for (let i = start; i <= end; i++) next.add(allIds[i]);
      } else {
        if (next.has(candidateId)) next.delete(candidateId);
        else next.add(candidateId);
      }
      return next;
    });
    setLastCheckedIdx(idx);
  }

  function handleCardClick(candidate) {
    setSelectedId(prev => prev === candidate.id ? null : candidate.id);
  }

  async function handleBatchDelete() {
    if (!confirm(`Delete ${checkedIds.size} selected images?`)) return;
    try {
      const updated = candidates.filter(c => !checkedIds.has(c.id));
      const res = await fetch(`/api/events/${event.id}`, { method: 'PATCH', headers: { 'Content-Type': 'application/json' }, body: JSON.stringify({ imageCandidates: updated }) });
      if (!res.ok) throw new Error((await res.json()).error);
      setCheckedIds(new Set());
      setSelectedId(null);
      onUpdate?.();
    } catch (err) { alert('Delete failed: ' + err.message); }
  }

  async function handleBatchUpscale() {
    const toUpscale = candidates.filter(c => checkedIds.has(c.id) && !c.upscaled);
    if (toUpscale.length === 0) { alert('No un-upscaled images selected.'); return; }
    const cost = (toUpscale.length * 0.05).toFixed(2);
    if (!confirm(`Upscale ${toUpscale.length} images using fal.ai (~$${cost} total). Continue?`)) return;
    setBatchUpscaling(true);
    try {
      for (const c of toUpscale) {
        await fetch('/api/assets/upscale', {
          method: 'POST',
          headers: { 'Content-Type': 'application/json' },
          body: JSON.stringify({ eventId: event.id, candidateId: c.id, imageUrl: c.url, scale: 2 }),
        });
      }
      onUpdate?.();
    } catch (err) { alert('Batch upscale failed: ' + err.message); }
    finally { setBatchUpscaling(false); }
  }

  async function confirmCrop(candidateId, cropOffsetX, aspectRatioStr) {
    try {
      const cropData = { offsetX: cropOffsetX, aspectRatio: aspectRatioStr };
      // Save crop data on the candidate AND mark as selected
      const res = await fetch(`/api/events/${event.id}/select-image`, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ candidateId, cropData }),
      });
      if (!res.ok) throw new Error((await res.json()).error);
      // Also update the candidate's crop data locally via PATCH
      const cands = [...(event.imageCandidates ?? [])];
      const idx = cands.findIndex(c => c.id === candidateId);
      if (idx >= 0) {
        cands[idx] = { ...cands[idx], cropData, cropped: true };
        await fetch(`/api/events/${event.id}`, {
          method: 'PATCH',
          headers: { 'Content-Type': 'application/json' },
          body: JSON.stringify({ imageCandidates: cands }),
        });
      }
      setCropCandidate(null);
      onUpdate?.();
    } catch (err) { alert('Crop failed: ' + err.message); }
  }

  const hasImage = event.imageCandidates?.some(c => c.selected) || event.selectedImageCandidate?.url;
  const showCheckboxes = checkedIds.size > 0;

  const categoryProps = { selectedId, checkedIds, onCheck: handleCheck, onClick: handleCardClick, onSetPrimary: handleSetPrimary, onRemove: handleRemoveImage, showCheckboxes };

  return (
    <div className="ep">
      {/* Top: Card Preview + Form */}
      <div className="ep-top">
        <CardPreview event={event} />
        <div className="ep-meta">
          <div className="ep-form">
            {field('title', 'Title')}
            <div className="ep-row">{field('venue', 'Venue')}{field('city', 'City')}</div>
            <div className="ep-row">{field('date', 'Date')}{field('startTime', 'Start')}{field('endTime', 'End')}</div>
            {field('mode', 'Mode', { select: true })}
            {field('description', 'Description', { textarea: true })}
          </div>
          <button className="ep-save" onClick={save} disabled={saving}>{saving ? 'Saving…' : '💾 Save'}</button>
          {error && <div className="ep-error">⚠ {error}</div>}
          <button className="ep-publish" onClick={handleSendToPublish} disabled={sending || !hasImage}>
            {sending ? '⏳ Sending…' : !hasImage ? '⚠ Select an image first' : '📅 Send to Publish Calendar'}
          </button>
        </div>
      </div>

      {/* Asset Studio */}
      <div className="studio">
        {/* Source Tabs */}
        <div className="src-tabs">
          <div className="src-tabs-bar">
            {[
              { id: 'scrape', icon: '🔎', label: 'Scrape' },
              { id: 'upload', icon: '📁', label: 'Upload' },
              { id: 'generate', icon: '✨', label: 'Generate' },
            ].map(t => (
              <button key={t.id} className={`src-tab ${sourceTab === t.id ? 'src-tab--active' : ''}`} onClick={() => setSourceTab(prev => prev === t.id ? null : t.id)}>
                {t.icon} {t.label}
              </button>
            ))}
            <div className="src-tabs-spacer" />
            {candidates.length > 0 && (
              <button className="src-clear-btn" onClick={async () => {
                if (!confirm('Remove all images?')) return;
                await fetch(`/api/events/${event.id}`, { method: 'PATCH', headers: { 'Content-Type': 'application/json' }, body: JSON.stringify({ imageCandidates: [] }) });
                onUpdate?.();
              }}>🗑 Clear All</button>
            )}
            <span className="src-count">{candidates.length} image{candidates.length !== 1 ? 's' : ''}</span>
          </div>
          {/* Expanded panel */}
          {sourceTab === 'scrape' && <ScrapePanel event={event} onUpdate={onUpdate} />}
          {sourceTab === 'upload' && <UploadPanel event={event} onUpdate={onUpdate} />}
          {sourceTab === 'generate' && <GeneratePanel event={event} onUpdate={onUpdate} />}
        </div>

        {/* Batch bar */}
        {checkedIds.size > 1 && <BatchBar count={checkedIds.size} onUpscaleAll={handleBatchUpscale} onDeleteAll={handleBatchDelete} upscaling={batchUpscaling} />}

        {/* Gallery + Action Panel side by side */}
        <div className="studio-body">
          <div className={`studio-gallery ${selectedCandidate ? 'studio-gallery--narrow' : ''}`}>
            {hasCategorized ? (
              <div className="gal-categories">
                <CategorySection label="Venue" icon="🏢" description={event.venue ? `Photos of ${event.venue}` : 'Venue photos'} images={venueImgs} {...categoryProps} />
                <CategorySection label="Event" icon="🎫" description={event.title ? `Related to "${event.title}"` : 'Event images'} images={eventImgs} {...categoryProps} />
                <CategorySection label="AI Generated" icon="🤖" description="AI-generated imagery" images={activityImgs} {...categoryProps} />
                {otherImgs.length > 0 && <CategorySection label="Other" icon="📷" description="Uncategorized" images={otherImgs} {...categoryProps} />}
              </div>
            ) : candidates.length > 0 ? (
              <div className="gal-grid gal-grid--flat">
                {candidates.map(c => (
                  <ImageCard key={c.id} c={c} isSelected={selectedId === c.id} isChecked={checkedIds.has(c.id)} onCheck={handleCheck} onClick={handleCardClick} onSetPrimary={handleSetPrimary} onRemove={handleRemoveImage} showCheckbox={showCheckboxes} />
                ))}
              </div>
            ) : (
              <div className="gal-empty">
                <div className="gal-empty-icon">🖼️</div>
                <div className="gal-empty-title">No images yet</div>
                <div className="gal-empty-desc">Use the tabs above to scrape, upload, or generate images.</div>
                {event.venue && <div className="gal-empty-hint">Will search for: {event.venue}{event.city ? `, ${event.city}` : ''}</div>}
              </div>
            )}
          </div>

          {/* Action panel */}
          {selectedCandidate && (
            <ActionPanel candidate={selectedCandidate} event={event} onUpdate={onUpdate} onClose={() => setSelectedId(null)} />
          )}
        </div>

        {/* Keyboard hint */}
        <div className="studio-shortcuts">
          <span>Shortcuts:</span>
          <kbd>Space</kbd> preview
          <kbd>U</kbd> upscale
          <kbd>C</kbd> crop
          <kbd>A</kbd> animate
          <kbd>Del</kbd> remove
          <kbd>Esc</kbd> deselect
        </div>
      </div>

      {/* Modals */}
      {cropCandidate && <CropModal candidate={cropCandidate} onSelect={confirmCrop} onClose={() => setCropCandidate(null)} />}
      {lightbox && <Lightbox candidate={lightbox} onClose={() => setLightbox(null)} onSelectForCrop={(c) => setCropCandidate(c)} />}
    </div>
  );
}

/* ── Main Page ── */
export default function AssetsPage() {
  const [allEvents, setAllEvents] = useState([]);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState(null);
  const [activeId, setActiveId] = useState(null);

  async function loadEvents() {
    const res = await fetch('/api/events?all=1');
    const data = await res.json();
    if (!res.ok) throw new Error(data.error ?? 'Failed');
    setAllEvents(data.events ?? []);
  }

  useEffect(() => { loadEvents().catch(e => setError(e.message)).finally(() => setLoading(false)); }, []);

  const queue = useMemo(() => allEvents.filter(e => e.status === 'approved_1'), [allEvents]);
  const activeEvent = queue.find(e => e.id === activeId) || null;

  useEffect(() => { if (!activeId && queue.length > 0) setActiveId(queue[0].id); }, [queue.length]);

  async function deleteFromQueue(eventId) {
    if (!confirm('Remove this event from the queue?')) return;
    try {
      const res = await fetch(`/api/events/${eventId}/review`, { method: 'POST', headers: { 'Content-Type': 'application/json' }, body: JSON.stringify({ action: 'reject', stage: 1, reviewedBy: 'admin', notes: 'Removed from asset queue' }) });
      if (!res.ok) throw new Error((await res.json()).error);
      if (activeId === eventId) setActiveId(null);
      await loadEvents();
    } catch (err) { alert('Delete failed: ' + err.message); }
  }

  return (
    <main className="admin-shell">
      <div className="admin-wrap">
        <header className="adash-header">
          <div className="adash-brand"><span className="adash-brand-ok">OK</span><br />LET&apos;S GO<br />/ ADMIN</div>
          <div className="adash-header-right">
            <span className="admin-pill">asset studio</span>
            <form method="POST" action="/api/admin/logout"><button type="submit" className="logout-btn">Logout</button></form>
          </div>
        </header>
        <AdminNav />

        {loading && <div className="ep-empty">⏳ Loading…</div>}
        {error && <div className="ep-empty">⚠️ {error}</div>}

        {!loading && !error && (
          <div className="assets-layout">
            <div className="queue">
              <div className="queue-head">Queue ({queue.length})</div>
              {queue.length === 0 && <div className="queue-empty">No events in asset lane</div>}
              {queue.map(e => <QueueItem key={e.id} event={e} active={e.id === activeId} onClick={() => setActiveId(e.id)} onDelete={deleteFromQueue} />)}
            </div>
            <EditPanel event={activeEvent} onUpdate={loadEvents} onSendToPublish={(id) => { if (activeId === id) setActiveId(null); }} />
          </div>
        )}
      </div>

      <style>{STYLES}</style>
    </main>
  );
}


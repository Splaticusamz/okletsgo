/* ── Action Panel (single image selected) ── */
function ActionPanel({ candidate, event, onUpdate, onClose }) {
  const [upscaling, setUpscaling] = useState(false);
  const [upscaleScale, setUpscaleScale] = useState(2);
  const [animating, setAnimating] = useState(false);
  const [animStatus, setAnimStatus] = useState(null);
  const [animRequestId, setAnimRequestId] = useState(null);
  const pollRef = useRef(null);
  const info = sourceInfo(candidate);

  // Cleanup poll on unmount
  useEffect(() => () => { if (pollRef.current) clearInterval(pollRef.current); }, []);

  async function handleUpscale() {
    if (!confirm(`Upscale ${upscaleScale}x using fal.ai (~$0.05). Continue?`)) return;
    setUpscaling(true);
    try {
      const res = await fetch('/api/assets/upscale', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ eventId: event.id, candidateId: candidate.id, imageUrl: candidate.url, scale: upscaleScale }),
      });
      if (!res.ok) throw new Error((await res.json()).error);
      onUpdate?.();
    } catch (err) { alert('Upscale failed: ' + err.message); }
    finally { setUpscaling(false); }
  }

  async function handleAnimate() {
    if (!confirm('Generate video animation using fal.ai (~$0.50). This is expensive! Continue?')) return;
    setAnimating(true);
    setAnimStatus('submitting');
    try {
      const res = await fetch('/api/assets/animate', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ eventId: event.id }),
      });
      if (!res.ok) throw new Error((await res.json()).error);
      const data = await res.json();
      setAnimRequestId(data.requestId);
      setAnimStatus('processing');
      // Start polling
      pollRef.current = setInterval(async () => {
        try {
          const pollRes = await fetch(`/api/assets/animate?eventId=${event.id}&requestId=${data.requestId}`);
          const pollData = await pollRes.json();
          if (pollData.status === 'completed') {
            clearInterval(pollRef.current);
            setAnimStatus('done');
            setAnimating(false);
            onUpdate?.();
          } else if (pollData.status === 'failed') {
            clearInterval(pollRef.current);
            setAnimStatus('failed');
            setAnimating(false);
          }
        } catch { /* keep polling */ }
      }, 5000);
    } catch (err) {
      alert('Animate failed: ' + err.message);
      setAnimating(false);
      setAnimStatus(null);
    }
  }

  return (
    <div className="action-panel">
      <div className="action-panel-header">
        <span className="action-panel-title">Image Actions</span>
        <button className="action-panel-close" onClick={onClose}>×</button>
      </div>
      <div className="action-panel-body">
        {/* Preview */}
        <div className="action-preview">
          <img src={candidate.url} alt="" style={candidate.cropData?.offsetX != null ? { objectPosition: `${candidate.cropData.offsetX}% 50%` } : undefined} />
        </div>

        {/* Info */}
        <div className="action-info">
          <span className={`gal-tag gal-tag--lg ${info.cls}`}>{info.icon} {info.tag}</span>
          <span className="action-info-desc">{info.desc}</span>
          {candidate.upscaled && <span className="action-info-badge">⬆ Upscaled {candidate.upscaleScale}x</span>}
          {candidate.originalUrl && <span className="action-info-detail">Original preserved</span>}
          {candidate.aiPrompt && <div className="action-info-prompt">Prompt: {candidate.aiPrompt.slice(0, 150)}</div>}
        </div>

        {/* Actions */}
        <div className="action-buttons">
          {/* Upscale */}
          <div className="action-group">
            <div className="action-group-label">⬆ Upscale</div>
            <div className="action-group-row">
              <div className="action-toggle">
                <button className={`action-toggle-btn ${upscaleScale === 2 ? 'action-toggle-btn--on' : ''}`} onClick={() => setUpscaleScale(2)}>2x</button>
                <button className={`action-toggle-btn ${upscaleScale === 4 ? 'action-toggle-btn--on' : ''}`} onClick={() => setUpscaleScale(4)}>4x</button>
              </div>
              <button className="action-btn action-btn--upscale" onClick={handleUpscale} disabled={upscaling}>
                {upscaling ? <><span className="gal-spinner" /> Upscaling…</> : `Upscale ${upscaleScale}x`}
              </button>
            </div>
          </div>

          {/* Crop */}
          <div className="action-group">
            <div className="action-group-label">✂️ Crop</div>
            <button className="action-btn action-btn--crop" onClick={() => {/* handled via CropModal in parent */}} data-action="crop">
              Open Crop Tool
            </button>
          </div>

          {/* Animate */}
          <div className="action-group">
            <div className="action-group-label">🎬 Animate</div>
            <button className="action-btn action-btn--animate" onClick={handleAnimate} disabled={animating}>
              {animStatus === 'processing' ? <><span className="gal-spinner" /> Processing…</> :
               animStatus === 'done' ? '✓ Animation Ready' :
               animStatus === 'failed' ? '✗ Failed — Retry' :
               '🎬 Generate Animation'}
            </button>
            {animStatus === 'processing' && <div className="action-hint">This can take a few minutes. You can keep working.</div>}
          </div>
        </div>
      </div>
    </div>
  );
}

